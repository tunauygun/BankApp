    private void withdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawButtonActionPerformed
        double validAmount = getValidAmount(amount.getText());
        amount.setText("");
        if (validAmount<=Double.parseDouble(credentials.userCredentials.get(credentials.accountNumber)[2])){
            String[] UserAccount = credentials.userCredentials.get(credentials.accountNumber);
            double CurrentBalance = Double.parseDouble(UserAccount[2]);
            double finalBalance = CurrentBalance-validAmount;
            UserAccount[2]=Double.toString(finalBalance);
            credentials.userCredentials.set(credentials.accountNumber, UserAccount);
            String displayBalance = String.format("%.2f",finalBalance);
            BalanceLabel.setText(displayBalance);
        } else {
            JOptionPane.showMessageDialog(null, "Sorry! You can't withdraw more than your current balance!");
        }
        
    }//GEN-LAST:event_withdrawButtonActionPerformed

    private void depositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositButtonActionPerformed
        double validAmount = getValidAmount(amount.getText());
        amount.setText("");
        String[] UserAccount = credentials.userCredentials.get(credentials.accountNumber);
        double CurrentBalance = Double.parseDouble(UserAccount[2]);
        double finalBalance = CurrentBalance+validAmount;
        UserAccount[2]=Double.toString(finalBalance);
        credentials.userCredentials.set(credentials.accountNumber, UserAccount);
        String displayBalance = String.format("%.2f",finalBalance);
        BalanceLabel.setText(displayBalance);
    }//GEN-LAST:event_depositButtonActionPerformed

