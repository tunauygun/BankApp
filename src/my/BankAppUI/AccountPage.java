    public double getValidAmount(String strAmount){
        try{
            double doubleAmount = Double.parseDouble(strAmount);
            if(doubleAmount<0){
                JOptionPane.showMessageDialog(null, "Invalid Amount Input! Please enter a positive amount.");
                return 0;
            } else {
                return doubleAmount;
            }
        }catch(NumberFormatException e){
             JOptionPane.showMessageDialog(null, "Invalid Amount Input! Please enter a numerical amount.");
            return 0; 
        }           
    }
    
